package com.wincom.publicmodel.utility;

public class TimeObj {
	private int Year;
	private int Month;
	private int Day;
	private int Hour;
	private int Minute;
	private int Second;
	public void setYear(int Year){
		this.Year=Year;
	}
	public int getYear(){
		return this.Year;
	}
	public void setMonth(int Month){
		this.Month=Month;
	}
	public int getMonth(){
		return this.Month;
	}
	public void setDay(int Day){
		this.Day=Day;
	}
	public int getDay(){
		return this.Day;
	}
	public void setHour(int Hour){
		this.Hour=Hour;
	}
	public int getHour(){
		return this.Hour;
	}
	public void setMinute(int Minute){
		this.Minute=Minute;
	}
	public int getMinute(){
		return this.Minute;
	}
	public void setSecond(int Second){
		this.Second=Second;
	}
	public int getSecond(){
		return this.Second;
	}
}
